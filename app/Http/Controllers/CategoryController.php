     */
    public function show($slug)
    {
        $category = DB::table("categories")
                ->whereSlug($slug)
                ->first();


        if (!$category) {
            abort(404);
        }

        $courses = DB::table("courses")
                        ->where('category_id', $category->id)
                        ->get();

        return view("categories.show", compact("category", "courses"));
    }

